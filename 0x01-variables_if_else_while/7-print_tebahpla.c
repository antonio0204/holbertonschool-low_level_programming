#include <stdio.h>
#include <stdlib.h>
#include <time.h>
/**
  * main - Runs the file and if the n > 0 prints positive and
  * n < 0 negative,else it will print 0
  * Return: Always 0 (Success)
  */
int main(void)
{
	char za;

	for (za = 122; za >= 97; za--)
	{
		putchar(za);
	}
	putchar('\n');
	return (0);
}
